// Global state variable
let db = {
    schools: [],
    pending: [],
    alerts: [],
    reminders: []
};

// Initialize state from localstorage or seed
function initDatabase() {
    if (!localStorage.getItem('eportal_schools')) {
        localStorage.setItem('eportal_schools', JSON.stringify(defaultSchoolsData));
    }
    if (!localStorage.getItem('eportal_pending')) {
        localStorage.setItem('eportal_pending', JSON.stringify(defaultPendingData));
    }
    if (!localStorage.getItem('eportal_reminders')) {
        localStorage.setItem('eportal_reminders', JSON.stringify([]));
    }

    db.schools = JSON.parse(localStorage.getItem('eportal_schools'));
    db.pending = JSON.parse(localStorage.getItem('eportal_pending'));
    db.reminders = JSON.parse(localStorage.getItem('eportal_reminders'));

    calculateAlerts();
}

// Calculate and cache alerts dynamically
function calculateAlerts() {
    db.alerts = [];
    const currentDate = new Date("2026-06-18"); // Lock logic to current simulation date

    db.schools.forEach(school => {
        // 1. Reported Blockers
        if (school.blocker && school.blocker !== "None") {
            db.alerts.push({
                type: "blocker",
                school_id: school.id,
                school_name: school.name,
                title: `Blocker: ${school.blocker}`,
                desc: school.blocker_details || `Active blocker reported by Head Master.`,
                date: school.last_update
            });
        }

        // 2. Delayed Work Progress (>30 days since last update and progress < 90)
        const lastUpd = new Date(school.last_update);
        const diffTime = Math.abs(currentDate - lastUpd);
        const diffDays = Math.ceil(diffTime / (1000 * 60 * 60 * 24));
        if (diffDays > 30 && school.progress < 90) {
            db.alerts.push({
                type: "delay",
                school_id: school.id,
                school_name: school.name,
                title: `Delayed Work Progress`,
                desc: `No status updates received for ${diffDays} days. Current progress: ${school.progress}%.`,
                date: school.last_update
            });
        }

        // 3. Missing Geo-tag
        if (school.geo_tag === "Missing") {
            db.alerts.push({
                type: "geotag",
                school_id: school.id,
                school_name: school.name,
                title: `Missing Geo-Tag coordinates`,
                desc: `Site photos cannot be verified automatically. Coordinates required.`,
                date: school.last_update
            });
        }

        if (school.task_status && school.task_status === "Pending HM Action") {
            db.alerts.push({
                type: "task",
                school_id: school.id,
                school_name: school.name,
                title: `CEO Task Assigned`,
                desc: school.task_description || `A new task has been assigned by the CEO for this school.`,
                date: school.task_assigned_at || school.last_update
            });
        }
    });

    // 4. Pending updates
    db.pending.forEach(p => {
        db.alerts.push({
            type: "pending",
            school_id: p.school_id,
            school_name: p.school_name,
            title: `Pending Sachiv Approval`,
            desc: `Progress update request (${p.old_progress}% → ${p.new_progress}%) waiting verification.`,
            date: p.submitted_at
        });
    });

    // 5. Reminder / blocker messages from Sachiv
    db.reminders.forEach(item => {
        db.alerts.push({
            type: item.alert_type || 'reminder',
            school_id: item.school_id,
            school_name: item.school_name,
            title: item.title,
            desc: item.message,
            date: item.created_at
        });
    });
}

// Save active state to localStorage
function saveDatabase() {
    localStorage.setItem('eportal_schools', JSON.stringify(db.schools));
    localStorage.setItem('eportal_pending', JSON.stringify(db.pending));
    localStorage.setItem('eportal_reminders', JSON.stringify(db.reminders));
    calculateAlerts();

    // Dispatch a custom event to notify individual page logic to refresh UI if needed
    window.dispatchEvent(new Event('db_updated'));
}
